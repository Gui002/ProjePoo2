package Model.VO;

/**
 *
 * @author Gui
 */
public class Funcionario_VO {
    String nome, contacto, senha;
    int id;

    public Funcionario_VO(String nome, String contacto, String senha, int id) {
        this.nome = nome;
        this.contacto = contacto;
        this.senha = senha;
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getContacto() {
        return contacto;
    }

    public void setContacto(String contacto) {
        this.contacto = contacto;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

   
}
